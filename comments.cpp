#include <iostream>

int main()
{
/* this is a comment
and it extends until the closing
star-slash comment mark */
std::cout << "Hello World!" << std::endl;

// this comment ends at the end of the line
std::cout << "That comment ended!" << std::endl;

// double-slash comments can be alone on a line
std::cout << "double-slash comments can be alone on a line" << std::endl;

/* as can slash-star comments */
std::cout << "as can slash-star comments" << std::endl;

return 0;
}
